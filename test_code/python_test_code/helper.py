def greet(name):
    """
    Returns a greeting string addressed to the provided name.

    This function takes a name as input and returns a greeting string.
    It's a straightforward utility function for creating personalized
    greeting messages.

    Parameters:
    name (str): The name to be included in the greeting message.

    Returns:
    str: A greeting message that includes the provided name.
    """
    return f"Hello, {name}!"
